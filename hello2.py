# 리스트
a = [1, 2, 3]
b = [4, 5, 6]
print(a + b) # [1, 2, 3, 4, 5, 6]

# print(a + 100) 

a = [1, 2, 3]
del a[1] # [1, 3]
print(a)


a = [1, 2, 3, 4, 5]
del a[2:] # 0,1,2 초과 삭제
print(a) 

a = [1, 2, 3]
a.append(4) # [1, 2, 3, 4] 요소 추가
print(a)

a = [1, 4, 3, 2]
a.sort() # [1, 2, 3, 4] 오름차순 정렬
# a.sort(-1) # [4, 3, 2, 1] 내림차순 정렬
print(a)
